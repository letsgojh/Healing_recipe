def run():
    print("[1] Qdrant 컬렉션 생성 여부 확인")
    init_collection_if_needed()

    points = []

    print("[2] 스트레스 해소법 임베딩 + PointStruct 생성")
    for idx, text in enumerate(STRESS_RELIEF_LIST):
        vec = embed_text(text)

        points.append(
            PointStruct(
                id=idx,
                vector=vec,
                payload={
                    # 🔴 여기 수정
                    "title": text,             # 사용자에게 보여줄 메인 문장
                    "description": None,       # 나중에 상세 설명 넣고 싶으면 추가
                    "persona_label": None,     # 원하면 미리 라벨링해도 됨
                    "cluster_id": None,
                    "symbol": None,            # 처음엔 없음, clustering 후 채움
                }
            )
        )

    print("[3] Qdrant DB에 업서트")
    upsert_reliefs(points)

    print("[완료] 스트레스 해소법 리스트가 Qdrant에 저장되었습니다.")

if __name__ == "__main__":
    run()